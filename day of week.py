
# prompt: determine the day of a week

day = float(input("Enter a number between 1 and 7: "))

if day == 1:
  print("Sunday")
elif day == 2:
  print("Monday")
elif day == 3:
  print("Tuesday")
elif day == 4:
  print("Wednesday")
elif day == 5:
  print("Thursday")
elif day == 6:
  print("Friday")
elif day == 7:
  print("Saturday")
else:
  print("Invalid input. Please enter a number between 1 and 7.")


